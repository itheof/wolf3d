#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		*((unsigned char *)dst + i) = *((unsigned char *)src + i);
		if ((unsigned char)c == *((unsigned char *)src + i))
			return ((unsigned char *)dst + (i + 1) * sizeof(unsigned char));
		i++;
	}
	return (NULL);
}
